export function transliterate(text: string): string {
	const map: Record<string, string> = {
		а: 'a',
		б: 'b',
		в: 'v',
		г: 'g',
		д: 'd',
		е: 'e',
		ё: 'yo',
		ж: 'zh',
		з: 'z',
		и: 'i',
		й: 'y',
		к: 'k',
		л: 'l',
		м: 'm',
		н: 'n',
		о: 'o',
		п: 'p',
		р: 'r',
		с: 's',
		т: 't',
		у: 'u',
		ф: 'f',
		х: 'h',
		ц: 'ts',
		ч: 'ch',
		ш: 'sh',
		щ: 'shch',
		ы: 'y',
		э: 'e',
		ю: 'yu',
		я: 'ya',
		ъ: '',
		ь: '',

		a: 'а',
		b: 'б',
		v: 'в',
		g: 'г',
		d: 'д',
		e: 'е',
		yo: 'ё',
		zh: 'ж',
		z: 'з',
		i: 'и',
		y: 'й',
		k: 'к',
		l: 'л',
		m: 'м',
		n: 'н',
		o: 'о',
		p: 'п',
		r: 'р',
		s: 'с',
		t: 'т',
		u: 'у',
		f: 'ф',
		h: 'х',
		ts: 'ц',
		ch: 'ч',
		sh: 'ш',
		shch: 'щ',
		yu: 'ю',
		ya: 'я',
	}

	return text
		.toLowerCase()
		.split('')
		.map((char) => map[char] || char)
		.join('')
}
